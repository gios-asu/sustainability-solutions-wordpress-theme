<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">

What are the admission requirements?
</h2>
</div></a>
<div id="collapseTwo" class="panel-collapse collapse">
<div class="panel-body"><strong>Bachelor’s degree</strong>
You must have earned a U.S. bachelor’s degree or higher from a regionally accredited institution or the equivalent of a U.S. bachelor’s degree from an international institution that is officially recognized by that country.
<strong>Equivalent to a B average</strong>
The School of Sustainability requires the equivalent of a minimum GPA of 3.0 in the last sixty semester hours or ninety quarter hours of undergraduate work. Given that this is an Executive Master’s for experienced professionals, work and life experience may be taken into account in lieu of minimum GPA or other aspects of past bachelor degree program experience. Applicants whose GPA is below 3.0 may be admitted on a provisional status. Please <a title="Email Jen Griffin" class="link-styles" href="mailto:jennifer.l.griffin@asu.edu">contact us</a> for more details if your GPA falls below the 3.0 requirement.
<strong>Work experience</strong>
Applicants to the Executive Master’s for Sustainability Leadership should be or are interested in being employed and ideally have experience managing or advancing some aspect of sustainability in their organizations, either directly or indirectly. Successful candidates will be expected to draw from and apply solutions to actual professional challenges and opportunities. Please <a title="Email Jen Griffin" class="link-styles" href="mailto:jennifer.l.griffin@asu.edu">contact us</a> for more details if you are currently not employed.
<strong>English proficiency</strong>
If you are from a country whose native language is not English (regardless of where you may now reside), you must provide proof of English proficiency. For more details about the English proficiency requirement or to determine if you qualify for an exemption, please review the <a title="Admissions English Proficiency" class="link-styles" href="https://students.asu.edu/graduate/proficiency?destination=node/16798">ASU Graduate Admissions English Proficiency</a> requirements or <a title="Email Jen Griffin" class="link-styles" href="mailto:jennifer.l.griffin@asu.edu">contact us</a> for further guidance.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseThree">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
What is the program structure?
</h2>
</div></a>
<div id="collapseThree" class="panel-collapse collapse">
<div class="panel-body">The Executive Master’s for Sustainability Leadership program is a 13-month (January to January) program based around four principal curriculum threads: leadership, strategy, communication and global context. Each thread is led by an industry leader and complemented by module instructors from a pool of academics and practitioners to ensure the content is at the leading edge of sustainability practice. Each student will work on a project pulled from their organization’s program to maximize the impact on the participant’s involvement back to their organization.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseFour">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">

For whom is this program designed?
</h2>
</div></a>
<div id="collapseFour" class="panel-collapse collapse">
<div class="panel-body">This Executive Master’s program is designed for currently employed mid-career professionals in or near the field of sustainability, or for those who would like to lead change within the pioneering field of sustainability. Participants are passionate about sustainability and driving transformation throughout their organization.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseFive">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
How much does the program cost?
</h2>
</div></a>
<div id="collapseFive" class="panel-collapse collapse">
<div class="panel-body">Exact tuition and fees for the 2016 program are still to be set, but will not exceed average pricing for other executive master’s and MBA programs across the country. The estimated cost of tuition and fees is expected to be between $48,000 and $63,000 depending on state of residency and eligibility for financial aid.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseSix">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
What does the program cost cover and not cover?
</h2>
</div></a>
<div id="collapseSix" class="panel-collapse collapse">
<div class="panel-body">Tuition and fees cover all program costs with the exception of airfare, immunization and visa costs for the three in-person workshop sessions and any equipment needs to access the online learning modules. The program cost also does not include the admissions application fee and graduation expenses.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseSeven">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
What is the time commitment?
</h2>
</div></a>
<div id="collapseSeven" class="panel-collapse collapse">
<div class="panel-body">The program places an emphasis on self-directed on-line learning that will require a typical time commitment of 20 hours per week. Because Executive Master’s students will be working on company projects, we envision that this time will intersect with their regular workday seamlessly. The program also includes three in-person workshop sessions: one three to five-day kickoff session at ASU in Tempe, Arizona, an internationally-based eight-day session mid-program and a three to five-day closing session in Tempe.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseEight">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
What are travel requirements?
</h2>
</div></a>
<div id="collapseEight" class="panel-collapse collapse">
<div class="panel-body">Aside from the three workshop sessions, no additional travel is required. The Julie Ann Wrigley Global Institute of Sustainability and School of Sustainability hosts regular <a title="sustainability events" target="_blank" class="link-styles" href="https://sustainability.asu.edu/events">lecture series, workshops and special conferences</a> that showcase current thought leaders and issues in the world of sustainability. All students are encouraged to participate in these events as they are able.</div>
</div>
</div>
<div class="panel panel-primary">
<a class="accordion-link" data-toggle="collapse" data-parent="#accordion" href="#collapseNine">
<div class="panel-heading">
<h2 style="padding-top:10px; padding-bottom:0" class="panel-title">
What if I already have a MBA or other master’s degree?
</h2>
</div></a>
<div id="collapseNine" class="panel-collapse collapse">
<div class="panel-body">This program’s focus is on organizational leadership, business strategy and change management through the lens of sustainability. Participants will develop both the knowledge and skills to negotiate, advocate and implement sustainability practices within their organizations. By building a portfolio of projects throughout the program, students will have immediate impact on their organization. The first cohort included students who hold master’s and PhD degrees.</div>
